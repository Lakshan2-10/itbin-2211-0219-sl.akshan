    private void proSearchTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_proSearchTableMouseClicked
        int index = proSearchTable.getSelectedRow();
        TableModel  model = proSearchTable.getModel();
        String productName = model.getValueAt(index, 0).toString();
        Product product = ProductDao.getProductByName(productName);
        proNameTb.setText(product.getName());
        proPriceTb.setText(product.getPrice());
        jSpinner1.setValue(1);
        totTb.setText(product.getPrice());
        productPrice = Integer.parseInt(product.getPrice());
        productTotal = Integer.parseInt(product.getPrice());
        addBtn.setEnabled(true);
        
    }//GEN-LAST:event_proSearchTableMouseClicked

    private void jSpinner1StateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jSpinner1StateChanged
       int quantity  = (Integer) jSpinner1.getValue();
       if(quantity <=1){
           jSpinner1.setValue(1);
           quantity = 1;
       }
       productTotal = productPrice * quantity;
       totTb.setText(String.valueOf(productTotal));
    }//GEN-LAST:event_jSpinner1StateChanged

