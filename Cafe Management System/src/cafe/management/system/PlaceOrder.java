    private void ProTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ProTableMouseClicked
        int index = ProTable.getSelectedRow();
        int a = JOptionPane.showConfirmDialog(null, "Do you want to remove this product", "Select", JOptionPane.YES_NO_OPTION);
        if(a==0){
            TableModel model = ProTable.getModel();
            String total = model.getValueAt(index, 3).toString();
            grandTotal = grandTotal - Integer.parseInt(total);
            lblGrand.setText(String.valueOf(grandTotal));
            ((DefaultTableModel) ProTable.getModel()).removeRow(index);
            
        }
    }//GEN-LAST:event_ProTableMouseClicked

